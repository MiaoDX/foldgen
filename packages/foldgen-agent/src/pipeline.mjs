function createStepDiagramSvg(preview, operation, step, stepNumber, executorOverlay) {
  const size = 360;
  const padding = 34;
  const projected = projectPreviewVertices(preview.vertices, size, padding);
  const activeKey = edgeKey(operation.edge);
  const lines = preview.edges.map((edge) => {
    const start = projected.get(edge.vertices[0]);
    const end = projected.get(edge.vertices[1]);
    if (!start || !end) {
      return "";
    }
    const isActive = edgeKey(edge.vertices) === activeKey;
    if (!isActive && edge.assignment !== "B") {
      return "";
    }
    const color = isActive ? "#f97316" : "#0f172a";
    const width = isActive ? 5 : 2.4;
    return `<line x1="${start.x}" y1="${start.y}" x2="${end.x}" y2="${end.y}" stroke="${color}" stroke-width="${width}" stroke-linecap="round"/>`;
  }).filter(Boolean);
  const [a, b] = operation.edge;
  const start = projected.get(a);
  const end = projected.get(b);
  const arrow = start && end ? motionArrow(start, end) : "";
  const midpoint = start && end ? {
    x: round((start.x + end.x) / 2),
    y: round((start.y + end.y) / 2)
  } : null;
  const activeLabel = midpoint
    ? `<text x="${midpoint.x + 10}" y="${midpoint.y - 10}" fill="#9a3412" font-family="Inter, Arial, sans-serif" font-size="11" font-weight="700">${escapeXml(operation.assignment)} fold line</text>`
    : "";
  return [
    `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 ${size} ${size}" role="img" aria-label="${escapeXml(step?.title ?? operation.name)}">`,
    `<rect width="${size}" height="${size}" rx="18" fill="#fffaf0"/>`,
    `<text x="22" y="34" fill="#334155" font-family="Inter, Arial, sans-serif" font-size="18" font-weight="700">Step ${stepNumber}</text>`,
    `<text x="22" y="58" fill="#64748b" font-family="Inter, Arial, sans-serif" font-size="12">orange line = current fold; contact overlay = selected executor</text>`,
    `<g transform="translate(0 20)">`,
    ...lines,
    ...(executorOverlay.svg_elements ?? []),
    arrow,
    activeLabel,
    `</g>`,
    `</svg>`
  ].join("");
}

