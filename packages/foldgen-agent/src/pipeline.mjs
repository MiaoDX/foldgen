async function createTargetMatchArtifact({ targetPath, foldedStatePath, outputPath, solverState }) {
  if (solverState?.status !== "passed") {
    const blocked = {
      type: "foldgen.target_match.v1",
      status: "blocked",
      metric: "silhouette-iou-v0",
      score: 0,
      threshold: 0.48,
      target_artifact_path: artifactPath(targetPath),
      folded_state_artifact_path: null,
      target_polygon_count: 0,
      folded_polygon_count: 0,
      blocker: "missing-solver-backed-folded-state",
      limitations: ["Target match cannot run without a solver-backed folded-state artifact."]
    };
    await writeJson(outputPath, blocked);
    return blocked;
  }
  return evaluateTargetMatch({ targetSvgPath: targetPath, foldedStatePath, outputPath });
}

function buildResultQuality({ target, externalValidation, completedTarget = false, displayDecision = null }) {
  const flatFolderStatus = externalValidation?.flat_folder?.status ?? "not-run";
  const solverStateStatus = externalValidation?.flat_folder_state?.status ?? "not-run";
  const targetMatchStatus = externalValidation?.target_match?.status ?? "not-run";
  const displayMode = displayDecision?.display_mode ?? "inspection-only";
  const foldabilityStatus = flatFolderStatus === "passed"
    ? "external-solver-passed"
    : flatFolderStatus === "failed"
      ? "external-solver-failed"
      : "external-solver-not-run";
  const previewStatus = solverStateStatus === "passed" ? "solver-backed-folded-state" : "2.5d-inspection-only";
  const targetName = target?.name ?? "target";
  const limitations = [
    "Target silhouette match has not been validated against the reference image."
  ];
  if (previewStatus !== "solver-backed-folded-state") {
    limitations.push("The preview is a deterministic 2.5D inspection rendering, not a physical folding simulation.");
  }
  if (foldabilityStatus === "external-solver-failed") {
    limitations.push("Flat-Folder did not validate this crease pattern as a solver-successful fold.");
  }
  if (solverStateStatus !== "passed") {
    limitations.push("No solver-backed folded-state artifact is available for completed-target display.");
  }
  if (solverStateStatus === "passed" && targetMatchStatus !== "passed") {
    limitations.push("Solver-backed folded state does not pass the target silhouette gate.");
  }
  if (displayDecision?.display_mode === "completed-3d-partial-walkthrough") {
    limitations.push("Final folded-state target passes, but at least one walkthrough step is not solver-backed.");
  }
  if (displayDecision?.display_mode === "completed-usable-generated") {
    limitations.push("This is a generated candidate graduation, not a known-good tutorial fixture or physical embodiment record.");
  }
  if (displayDecision?.display_mode === "blocked-executor") {
    limitations.push("Geometry gates pass, but selected executor overlay evidence is incomplete or unsupported.");
  }
  return {
    target_match_status: targetMatchQualityStatus(targetMatchStatus, solverStateStatus),
    foldability_status: foldabilityStatus,
    preview_status: previewStatus,
    display_mode: displayMode,
    display_status: displayDecision?.display_status ?? "inspection-only",
    physical_result_status: "not-proven",
    summary: resultQualitySummary({ targetName, completedTarget, displayDecision }),
    limitations
  };
}

function resultQualitySummary({ targetName, completedTarget, displayDecision }) {
  if (displayDecision?.display_mode === "completed-usable-generated") {
    return `${targetName} is a generated candidate that passed backend folded-state geometry, target match, complete step replay, and executor feasibility gates.`;
  }
  if (displayDecision?.display_mode === "completed-usable") {
    return `${targetName} has solver-backed folded-state geometry, passes target match, and has complete step-state plus executor overlay evidence.`;
  }
  if (displayDecision?.display_mode === "completed-3d-partial-walkthrough") {
    return `${targetName} has solver-backed folded-state geometry and passes target match, but is not a completed usable walkthrough yet.`;
  }
  if (completedTarget) {
    return `${targetName} has solver-backed folded-state geometry and passes the silhouette target-match gate.`;
  }
  return `${targetName} is blocked from completed-target display until solver-backed folded-state geometry exists.`;
}

