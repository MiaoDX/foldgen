function normalizeOperations(candidateConfig) {
  if (Array.isArray(candidateConfig.operations) && candidateConfig.operations.length > 0) {
    return candidateConfig.operations;
  }
  if (candidateConfig.operation) {
    return [candidateConfig.operation];
  }
  throw new Error(`${candidateConfig.id}: missing operations`);
}

function isGeneratedCandidate(selected) {
  return selected?.origin_kind === "generated-candidate";
}

function generatedStatusForDisplayMode(displayMode) {
  if (displayMode === "completed-usable-generated") {
    return "graduated";
  }
  if (displayMode?.startsWith("blocked-") || displayMode?.startsWith("rejected-")) {
    return "rejected";
  }
  return "not-generated";
}

