function solverBlockedReason({ flatFolderValidation, flatFolderState, targetMatch }) {
  if (flatFolderValidation?.status !== "passed") {
    return "flat-folder-validation-failed";
  }
  if (flatFolderState?.status !== "passed") {
    return "solver-backed-folded-state-missing";
  }
  if (targetMatch?.status !== "passed") {
    return targetMatch?.status === "failed" ? "target-match-failed" : "target-match-missing";
  }
  return "completed-target-gate-failed";
}

async function writeJson(path, value) {
  await writeFile(path, `${stableStringify(value, 2)}\n`, "utf8");
}

function artifactPath(path) {
  return toPosix(relative(process.cwd(), path));
}

function toPosix(path) {
  return path.split("\\").join("/");
}

function colorForAssignment(assignment) {
  switch (assignment) {
    case "B":
      return "#0f172a";
    case "M":
      return "#dc2626";
    case "V":
      return "#2563eb";
    case "F":
      return "#64748b";
    default:
      return "#94a3b8";
  }
}

function referenceEdgeColor(assignment) {
  return assignment === "B" ? "#0f172a" : "#94a3b8";
}

function edgeKey(edge) {
  return [...edge].sort((a, b) => a - b).join(":");
}

function round(value) {
  return Number(value.toFixed(3));
}

function escapeXml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function cloneJson(value) {
  return JSON.parse(JSON.stringify(value));
}
