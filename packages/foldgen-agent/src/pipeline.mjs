function createStepVisuals({ caseId, target, operations, animation, sequence, profileSequences = null, stepStates = null }) {
  const frames = animation.frames.slice(1);
  const sequencesByProfile = normalizeStepVisualSequences(sequence, profileSequences);
  const profileSteps = Object.fromEntries(Object.entries(sequencesByProfile).map(([executorProfile, profileSequence]) => [
    executorProfile,
    operations.map((operation, index) => {
      const step = profileSequence.steps[index];
      const frame = frames[index] ?? animation.frames[0];
      return createStepVisual({
        operation,
        step,
        frame,
        stepNumber: index + 1,
        executorProfile,
        stepState: stepStates?.states?.[index] ?? null
      });
    })
  ]));
  const defaultSteps = profileSteps["human-hand"] ?? Object.values(profileSteps)[0] ?? [];
  const hasAllSolverBackedSteps = stepStates?.status === "complete";
  return {
    type: "foldgen.step_visuals.v1",
    case_id: caseId,
    display_source: hasAllSolverBackedSteps ? "flat-folder-step-state" : "mixed-step-state-and-inspection-preview",
    display_source_status: stepStates?.status === "complete"
      ? "solver-backed-post-states"
      : stepStates?.status === "partial"
        ? "partial-solver-backed-post-states"
        : "inspection-only-until-step-state-backend",
    step_state_artifact_path: stepStates?.output_path ?? null,
    solver_backed_step_count: stepStates?.solver_backed_step_count ?? 0,
    inspection_only_step_count: stepStates?.inspection_only_step_count ?? operations.length,
    target: {
      name: target.name,
      file: target.file
    },
    step_count: operations.length,
    annotation_legend: stepVisualLegend(),
    steps: defaultSteps,
    profile_steps: profileSteps
  };
}

function normalizeStepVisualSequences(sequence, profileSequences) {
  if (profileSequences) {
    return Object.fromEntries(Object.entries(profileSequences).map(([profile, value]) => [
      profile,
      value.sequence ?? value
    ]));
  }
  return {
    [sequence?.executor_profile ?? "human-hand"]: sequence
  };
}

